const codigoEscaneado = "100";
const idProducto = 100;

// Comparación con == (comparación de valor)
if (codigoEscaneado == idProducto) {
    console.log("El producto es correcto (==)");
} else {
    console.log("El producto es incorrecto (==)");
}

// Comparación con === (comparación de valor y tipo)
if (codigoEscaneado === idProducto) {
    console.log("El producto es correcto (===)");   
} else {
    console.log("El producto es incorrecto (===)");
}   

